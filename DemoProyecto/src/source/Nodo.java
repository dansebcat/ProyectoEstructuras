public class Nodo {
    
    private Nodo enlace;
    private Equipo equipo;

    public Nodo(Equipo equipo) {
        this.equipo = equipo;
        enlace=null;
    }

    public Nodo getEnlace() {
        return enlace;
    }

    public void setEnlace(Nodo enlace) {
        this.enlace = enlace;
    }

    public Equipo getEquipo() {
        return equipo;
    }

    public void setEquipo(Equipo equipo) {
        this.equipo = equipo;
    }
    
    
}
